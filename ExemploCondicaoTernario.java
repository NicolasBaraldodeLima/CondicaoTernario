public class ExemploCondicaoTernario {

    public static void main (String args[]) {
        int numero1 = 10;
        System.out.println(numero1 != 10 ? "é diferente de 10" : "é igual mesmo  10");


        int numero2 = 10;
        System.out.println(numero2 == 10 ? "é diferente de 10" : "é igual a 10");
    }
}
